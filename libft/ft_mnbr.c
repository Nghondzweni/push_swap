void	ft_mnbr(char *num, int len, int n)
{
	long	copy;
	int		i;

	i = 0;
	copy = n;
	num[len] = '\0';
	if (copy < 0)
	{
		num[i] = '-';
		i++;
		copy = copy * -1;
	}
	while (copy != 0)
	{
		len--;
		num[len] = ((copy % 10) + '0');
		copy = copy / 10;
	}
}
