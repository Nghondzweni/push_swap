int	ft_sqrt(int nb)
{
	int r;

	r = 1;
	if (nb < 0 || nb == 0)
	{
		return (0);
	}
	while (r < 46341)
	{
		if ((r * r) == nb)
		{
			return (r);
		}
		r = (r + 1);
	}
	return (0);
}
